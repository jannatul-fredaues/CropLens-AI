import tensorflow as tf
import math

IMG_SIZE = (224,224)
BATCH_SIZE = 32
seed_value = 123 # For reproducibility

dataset_base_path = "/content/drive/MyDrive/Croplens/Dataset"

# Get class names and total number of images by loading the dataset temporarily
# without batching to easily count.
temp_ds_for_info = tf.keras.utils.image_dataset_from_directory(
    dataset_base_path,
    image_size=IMG_SIZE,
    batch_size=None, # Load as individual samples to get total count easily
    shuffle=False # Order doesn't matter for counting
)

class_names = temp_ds_for_info.class_names
NUM_CLASSES = len(class_names)
total_images = tf.data.experimental.cardinality(temp_ds_for_info).numpy()

# Now load the dataset to be split, ensuring it's shuffled for random distribution
# and unbatched so that take/skip work on individual samples.
shuffled_unbatched_ds = tf.keras.utils.image_dataset_from_directory(
    dataset_base_path,
    image_size=IMG_SIZE,
    batch_size=None, # Load as individual samples
    shuffle=True, # Shuffle for random split
    seed=seed_value
)

# Define split ratios
TRAIN_RATIO = 0.7
VALIDATION_RATIO = 0.15
TEST_RATIO = 0.15

# Calculate number of samples for each split
train_size = math.floor(total_images * TRAIN_RATIO)
val_size = math.floor(total_images * VALIDATION_RATIO)
test_size = total_images - train_size - val_size # Assign remaining to test to ensure sum matches total

# Create train_ds, val_ds, test_ds
train_ds = shuffled_unbatched_ds.take(train_size).batch(BATCH_SIZE).prefetch(tf.data.AUTOTUNE)
remaining_after_train = shuffled_unbatched_ds.skip(train_size)

val_ds = remaining_after_train.take(val_size).batch(BATCH_SIZE).prefetch(tf.data.AUTOTUNE)
test_ds = remaining_after_train.skip(val_size).batch(BATCH_SIZE).prefetch(tf.data.AUTOTUNE)

print(f"Total images: {total_images}")
print(f"Number of classes: {NUM_CLASSES}")
print(f"Class names: {class_names}")
print(f"Train images: {train_size}")
print(f"Validation images: {val_size}")
print(f"Test images: {test_size}")

