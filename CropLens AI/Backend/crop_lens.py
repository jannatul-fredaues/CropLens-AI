import matplotlib.pyplot as plt

def plot_accuracy(history, title):

    plt.figure(figsize=(8,5))

    plt.plot(
        history.history['accuracy'],
        label='Train'
    )

    plt.plot(
        history.history['val_accuracy'],
        label='Validation'
    )

    plt.title(title)
    plt.xlabel("Epoch")
    plt.ylabel("Accuracy")
    plt.legend()
    plt.show()

